def calculate_gpa(subject, mid, attendance, ct, final):
  total_mark = mid + attendance + ct + final
  grade_mapping = {80: 4, 75: 3.75, 70: 3.5, 65: 3.25, 60: 3, 55: 2.75, 50: 2.5, 45: 2.25, 40: 2}
  gpa = grade_mapping.get(total_mark, "Failed")  # Use get() with default for "Failed"
  print(f"Your total marks in {subject} is - {total_mark}")
  if gpa != "Failed":
    print(f"Your GPA in {subject} is - {gpa}")
  return gpa

subjects = ["DSA", "Discrete Mathematics", "Math", "EEE", "Economics", "Ethics"]
subject_gpas = []
for subject in subjects:
  subject_mid = int(input(f"Enter your mid ter mark in {subject}: "))
  subject_attendance = int(input(f"Enter your attendance mark in {subject}: "))
  subject_ct = int(input(f"Enter your CT mark in {subject}: "))
  subject_final = int(input(f"Enter your final mark in {subject}: "))
  subject_gpa = calculate_gpa(subject, subject_mid, subject_attendance, subject_ct, subject_final)
  subject_gpas.append(subject_gpa)

# Assuming all subjects are considered for CGPA calculation
cgpa = sum(subject_gpas) / len(subjects)
print(f"Your CGPA is - {cgpa}")

